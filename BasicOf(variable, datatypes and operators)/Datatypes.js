// Types of datatypes

//1.Number datatype

var n = 100
console.log(n)
console.log(typeof(n))

//2. String datatype

let name = 'Rakshitha'
let message = 'Hello World!!!'
console.log(name)
console.log(message)
console.log(typeof(name))

//3. Boolean datatype

let isAdmin = true;
let isLoggedIn = false;
console.log(isAdmin)
console.log(typeof(isAdmin))
console.log(isLoggedIn)

//4. Null datatype

let s = null
console.log(s)
console.log(typeof(s))

//5. Undefined datatype

let y
console.log(y)
console.log(typeof(y))